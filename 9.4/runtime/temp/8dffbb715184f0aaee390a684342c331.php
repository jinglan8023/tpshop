        //总计金额
        function countCarMoney(){
            var sum=0.00;
            //通过name 属性获取
            $('[name^=car]').each(function(){
                if($(this).prop('checked')==true){
                var this_price = $(this).parents('tr').find('td').eq(4).find('span').text().replace(/,/g,'');
                    sum+=parseFloat(this_price);
