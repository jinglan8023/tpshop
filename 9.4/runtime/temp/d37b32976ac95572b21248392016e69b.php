        function getAjaxPage(type,order_field,order_type){
            //获取是否选中品牌
           var brand_id=$('.brand').find('.now').attr('brand_id');
            var price=$('.price').find('.now').text();
            //console.log(brand_id,price);

            var data='';
            if(brand_id!=''&&brand_id!=undefined){
                data='brand_id='+brand_id
            }
            if(price!=''&&price!=undefined){
                if(data!=''){
                    data+='&';
                }
                data+= '&price='+price;
            }
            //console.log(data);
            if(order_field != ''){
                if(data != ''){
                    data += '&';
                }
                    data+='order_field='+order_field+'&order_type='+order_type;
            }
            //点击品牌 价格随之变化
            if(type==1){
                $.ajax({
                    url:"<?php echo url('Product/showMoneySelect',['cate_id'=>request()->param('cate_id')]); ?>",
                    data:data,
                    type:'post',
                    dataType:'json',
                    success:function(message){
                        //console.log(message);
                        $('#productList').html(message);
                    }
                });
            }

            //ajax 请求筛选对应品牌下边的商品
            $.ajax({
                url:"<?php echo url('Product/productList',['cate_id'=>request()->param('cate_id')]); ?>",
                data:data,
                type:'post',
                dataType:'json',
                success:function(message){
                    $('#productList').html(message);
                }
            })

        }

